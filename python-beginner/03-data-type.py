# Tipe Data
# Text - str
# Numerik - int, float, complex
# Sequence - list, tuple, range
# Mapping - dict
# Set - set, frozenset
# Boolean - bool
# Binary - bytes, bytearray, memoryview
# None - NoneType

# str
gretting = "Hello World"
print(type(gretting))

# float
size = 15.5
print(type(size))

# int
age = 20
print(type(age))

# complex
cmplx = 1j
print(type(cmplx))

# list
array = ["apple", "banana", "manggo"]
print(type(array))

# tupple
tpl = ("asraf", "dika", "habib")
print(type(tpl))

# dict
hero = {"power": 100, size: "big"}
print(type(hero))

# frozenset
forz = frozenset({"apple", "banana", "apple"})
print(type(forz))

# bool
answare = True
print(type(answare))

# byte
say = b"hello"
print(type(say))

# bytearray
array_byte = bytearray(5)
print(array_byte)

# memoryview
memory = memoryview(bytes(5))

# none
none = None
print(type(none))
